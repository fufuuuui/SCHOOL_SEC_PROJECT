    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
                int value = Integer.parseInt(jTextField1.getText());
                float C;
                float F;
                float K;
                
                
                if (jButton1 != null){
                    // Calculate the Celsius and Convert it with the option selected
                    if(jComboBox1.getSelectedItem().equals("Celsius"))
                    {
                        // Calculate the Celsius converting it to Kelvin
                        if (jComboBox2.getSelectedItem().equals("Kelvin"))
                        {
                            K = (float) (value + 273.15);
                            jTextArea1.append(String.valueOf(K + " K"  + "\n"));                            
                        }
          
                         // Calculate the Celsius converting it to Fahrenheit
                        else if (jComboBox2.getSelectedItem().equals("Fahrenheit"))
                        {
                            C = (float) ((value * 5/9 ) + 32);
                            jTextArea1.append(String.valueOf(C+ " F"  + "\n"));                         
                        }else {JOptionPane.showMessageDialog(this,  "Please Do not select the same Temperature");}                       
                    }
                    
                     // Calculate the Fahrenheit and Convert it with the option selected
                    else if(jComboBox1.getSelectedItem().equals("Fahrenheit"))
                    {
                         // Calculate the Fahrenheit converting it to Celsius
                        if (jComboBox2.getSelectedItem().equals("Celsius"))
                        {
                            C = (float) ((value + 32) * 5/9);
                            jTextArea1.append(String.valueOf(C+ " C"   + "\n"));                           
                        }
                         // Calculate the Fahrenheit converting it to Kelvin
                        else if (jComboBox2.getSelectedItem().equals("Kelvin"))
                        {
                            K = (float) ((value + 32) * 5/9 + 273.15);
                            jTextArea1.append(String.valueOf(K+ " K"   + "\n"));                           
                        }else {JOptionPane.showMessageDialog(this,  "Please Do not select the same Temperature");}
                    }
                    
                     // Calculate the Kelvin and Convert it with the option selected
                    else if(jComboBox1.getSelectedItem().equals("Kelvin"))
                    {
                        // Calculate the Kelvin converting it to Celsius
                        if (jComboBox2.getSelectedItem().equals("Celsius"))
                        {
                            C = (float) (value - 273.15);
                            jTextArea1.append(String.valueOf(C+ " C"   + "\n"));
                            
                        }
                         // Calculate the Kelvin converting it to Celsius
                        else  if (jComboBox2.getSelectedItem().equals("Fahrenheit"))
                        {
                            F = (float) ((value + 32) * 5/9 + + 32);
                            jTextArea1.append(String.valueOf(F+ " F"   + "\n"));                           
                        }else {JOptionPane.showMessageDialog(this,  "Please Do not select the same Temperature");}
                    }
                    else if (jComboBox1.getSelectedItem().equals(jComboBox2.getSelectedItem()))
                    {
                        jTextArea1.append("Must Select Different Converter");             
                    }
                    else
                    {
                            JOptionPane.showMessageDialog(this,"Error Please Select an Operation" ,
                                    "Error" , JOptionPane.ERROR_MESSAGE);
                     }     
                }
        }catch(Exception e)
                {
                JOptionPane.showMessageDialog(this, "Error! must Enter a value or Converter");
                }
    }//GEN-LAST:event_jButton1ActionPerformed

