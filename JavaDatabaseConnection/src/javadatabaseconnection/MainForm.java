    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
       String name = studentName.getText();
       String strand = studentStrand.getText();
       String gender = maleGender.isSelected()? "Male" : "Female";
       int grade_level = Integer.parseInt (studentGradeLevel.getSelectedItem().toString());
       controller.addStudent(name,gender,strand, grade_level);
       refreshTable();
       
    }//GEN-LAST:event_addBtnActionPerformed

    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        if (selectedStudentID != -1){
            String name = studentName.getText();
            String gender = maleGender.isSelected() ? "Male": "Female";
            String strand = studentStrand.getText();
            int grade_level = Integer.parseInt (studentGradeLevel.getSelectedItem().toString());
            
            controller.updateStudent(selectedStudentID, name, gender, strand, grade_level);
            refreshTable();
        } else {
            JOptionPane.showMessageDialog(this, "Please select a student from the table.");
        }
    }//GEN-LAST:event_editBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        if (selectedStudentID != -1) {
            int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this student?", "Confirm Delete"
                    , JOptionPane.YES_NO_OPTION);
            
            if (confirm == JOptionPane.YES_OPTION) {
                controller.deleteStudents(selectedStudentID);
                
                refreshTable();
                clearFields();
                selectedStudentID = -1;
            } else{
                JOptionPane.showMessageDialog (this, "Please select a student from the table first.");
            }
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void studentTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_studentTableMouseClicked
       int i = studentTable.getSelectedRow();
       TableModel model = studentTable.getModel();
       
       selectedStudentID = Integer.parseInt(model.getValueAt(1, 0).toString());
       studentName.setText(model.getValueAt(i, 1).toString());
       String gender = model.getValueAt(i, 2).toString();
       if (gender.equalsIgnoreCase("Male")) maleGender.setSelected(true);
       else femaleGender.setSelected(true);
       studentStrand.setText(model.getValueAt(i, 3).toString());
       studentGradeLevel.setSelectedItem(model.getValueAt(i, 4).toString());
        
    }//GEN-LAST:event_studentTableMouseClicked

