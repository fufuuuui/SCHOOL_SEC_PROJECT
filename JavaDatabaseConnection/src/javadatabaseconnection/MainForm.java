     public void refreshTable(){
         javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) studentTable.getModel();
         model.setRowCount(0);
         
         try(ResultSet rs = controller.getStudentData()){
             while (rs.next()){
                 Object [] row = {
                     
                     rs.getString ("name"),
                     rs.getString ("gender"),
                     rs.getString ("strand"),
                     rs.getInt ("grade_level"),
                
                 };
                 model.addRow(row);
             }
         } catch(SQLException e){
             System.out.println("Table Refresh Error: "+e.getMessage());
         }
             
     }
   public void updateStudent (int id, String name,String gender, String strand, int grade_level){
        String sql = "Update students set name =?, gender=?, strand=?, grade_level=? where student_id=?";
        executeSQL(sql, "Updated", name, gender, strand, grade_level, id);
    }
   public void deleteStudents (int id){
            String sql = "delete from students where student_id =?";
            try (Connection con = DBConnection.getConnection();
                 PreparedStatement pst = con.prepareStatement(sql)){
                pst.setInt (1, id);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Student Deleted Successfully!");
            } catch (SQLException e){
                System.out.println("Delete Error: " + e.getMessage());
                
            }
        }
    
